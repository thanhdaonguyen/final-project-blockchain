#!/bin/bash

function createUET() {
  infoln "Enrolling the CA admin"
  mkdir -p organizations/peerOrganizations/uet/

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/peerOrganizations/uet/

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:7054 --caname ca-uet --tls.certfiles "${PWD}/organizations/fabric-ca/uet/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-uet.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-uet.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-uet.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-uet.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/peerOrganizations/uet/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy uet's CA cert to uet's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/peerOrganizations/uet/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/uet/ca-cert.pem" "${PWD}/organizations/peerOrganizations/uet/msp/tlscacerts/ca.crt"

  # Copy uet's CA cert to uet's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/uet/tlsca"
  cp "${PWD}/organizations/fabric-ca/uet/ca-cert.pem" "${PWD}/organizations/peerOrganizations/uet/tlsca/tlsca.uet-cert.pem"

  # Copy uet's CA cert to uet's /ca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/uet/ca"
  cp "${PWD}/organizations/fabric-ca/uet/ca-cert.pem" "${PWD}/organizations/peerOrganizations/uet/ca/ca.uet-cert.pem"

  infoln "Registering peer0"
  set -x
  fabric-ca-client register --caname ca-uet --id.name peer0 --id.secret peer0pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/uet/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering user"
  set -x
  fabric-ca-client register --caname ca-uet --id.name user1 --id.secret user1pw --id.type client --tls.certfiles "${PWD}/organizations/fabric-ca/uet/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering the org admin"
  set -x
  fabric-ca-client register --caname ca-uet --id.name uetadmin --id.secret uetadminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/uet/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Generating the peer0 msp"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-uet -M "${PWD}/organizations/peerOrganizations/uet/peers/peer0.uet/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/uet/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/uet/msp/config.yaml" "${PWD}/organizations/peerOrganizations/uet/peers/peer0.uet/msp/config.yaml"

  infoln "Generating the peer0-tls certificates, use --csr.hosts to specify Subject Alternative Names"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-uet -M "${PWD}/organizations/peerOrganizations/uet/peers/peer0.uet/tls" --enrollment.profile tls --csr.hosts peer0.uet --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/uet/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  cp "${PWD}/organizations/peerOrganizations/uet/peers/peer0.uet/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/uet/peers/peer0.uet/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/uet/peers/peer0.uet/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/uet/peers/peer0.uet/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/uet/peers/peer0.uet/tls/keystore/"* "${PWD}/organizations/peerOrganizations/uet/peers/peer0.uet/tls/server.key"

  infoln "Generating the user msp"
  set -x
  fabric-ca-client enroll -u https://user1:user1pw@localhost:7054 --caname ca-uet -M "${PWD}/organizations/peerOrganizations/uet/users/User1@uet/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/uet/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/uet/msp/config.yaml" "${PWD}/organizations/peerOrganizations/uet/users/User1@uet/msp/config.yaml"

  infoln "Generating the org admin msp"
  set -x
  fabric-ca-client enroll -u https://uetadmin:uetadminpw@localhost:7054 --caname ca-uet -M "${PWD}/organizations/peerOrganizations/uet/users/Admin@uet/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/uet/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/uet/msp/config.yaml" "${PWD}/organizations/peerOrganizations/uet/users/Admin@uet/msp/config.yaml"
}

